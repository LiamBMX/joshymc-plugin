def clamp(v): return max(0, min(255, int(v)))

def draw_fish(fid, shape_key, primary, accent, belly):
    img = Image.new('RGBA', (16, 16), (0, 0, 0, 0))
    shape = SHAPES[shape_key]
    rng = random.Random(fid)
    body_rows = [i for i in range(16) if '#' in shape[i] or 'O' in shape[i] or 'o' in shape[i]]
    row_min = body_rows[0] if body_rows else 0
    row_max = body_rows[-1] if body_rows else 15
    row_span = max(1, row_max - row_min)

    for y in range(16):
        for x in range(16):
            ch = shape[y][x]
            if ch in ('#', 'O', 'o'):
                t = (y - row_min) / row_span
                r = primary[0] * (1 - t) + belly[0] * t
                g = primary[1] * (1 - t) + belly[1] * t
                b = primary[2] * (1 - t) + belly[2] * t
                r = clamp(r + rng.randint(-15, 15))
                g = clamp(g + rng.randint(-15, 15))
                b = clamp(b + rng.randint(-15, 15))

                # Edge detection for outline
                above = shape[y-1][x] if y > 0 else '.'
                below = shape[y+1][x] if y < 15 else '.'
                left = shape[y][x-1] if x > 0 else '.'
                right = shape[y][x+1] if x < 15 else '.'
                is_edge = above == '.' or below == '.' or left == '.' or right == '.'

                if is_edge:
                    r = clamp(accent[0] + rng.randint(-10, 10))
                    g = clamp(accent[1] + rng.randint(-10, 10))
                    b = clamp(accent[2] + rng.randint(-10, 10))

                if ch == 'O':
                    img.putpixel((x, y), (240, 240, 240, 255))
                    if x + 1 < 16: img.putpixel((x + 1, y), (10, 10, 10, 255))
                elif ch == 'o':
                    img.putpixel((x, y), (255, 200, 50, 255))  # lure glow
                else:
                    img.putpixel((x, y), (r, g, b, 255))

    img.save(os.path.join(base_dir, fid + '.png'))

for fid, shape, primary, accent, belly in fish_defs:
    draw_fish(fid, shape, primary, accent, belly)

